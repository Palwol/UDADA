
public class Calculation {

	public static void main(String[] args) {
		int a = 100, b = 50;
		
		int result = a+b;
		System.out.println(a + "+" + b + "=" + result);
		
		result = a-b;
		System.out.println(a + "-" + b + "=" + result);
		
		result = a*b;
		System.out.println(a + "*" + b + "=" + result);
		
		result = a/b;
		System.out.println(a + "/" + b + "=" + result);
	}

}
